# ============================================
# インデックスの使い方

# 要素を取り出す

alpha = ["a","b","c","d","e","f"]
alpha[2,3] = ["C","D","E"]
p alpha

# ============================================
# 複数のインデックスから配列を作る
# a.valies_at(n1,n2,…)

alpha = %w(a b c d e f)
p alpha.values_at(1,3,5)

