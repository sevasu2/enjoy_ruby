# ============================================
# 「|」と「+」の違い

num = [1,2,3]
even = [2,4,6]

p(num + even)
p(num | even)

# ============================================
# 配列の主なメソッド

# 配列に要素を加える

a = [1,2,3,4,5]
a.unshift(0)
p a

# ============================================
# 配列から要素を取り除く

a = [1,2,3,4,5]
a.delete_if {|i| i > 3}
p a
